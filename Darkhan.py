# Вычитание
result_subtraction = 10 - 3
print("Вычитание: 10 - 3 =", result_subtraction)

# Сложение
result_addition = 7 + 5
print("Сложение: 7 + 5 =", result_addition)

# Умножение
result_multiplication = 4 * 6
print("Умножение: 4 * 6 =", result_multiplication)

# Возведение в степень
result_power = 2 ** 3
print("Степень: 2 ** 3 =", result_power)

# Деление
result_division = 15 / 4
print("Деление: 15 / 4 =", result_division)

# Целочисленное деление
result_floor_division = 15 // 4
print("Целочисленное деление: 15 // 4 =", result_floor_division)

# Остаток от деления
result_modulo = 15 % 4
print("Остаток от деления: 15 % 4 =", result_modulo)